	    <h2>Reseña  Histórica</h2>
	    <p>
		El Colegio fue fundado en la ciudad de Trujillo por la Congregación Religiosa del Sagrado  de Jesús Corazón, 
		un 28 de marzo de 1949, a solicitud de un comité de Padres de Familia, presidido por la Sra. Mará Luisa Gallour 
		de Orbegoso; e integrado por la Sra. Etelvina de Yturri y los Srs. Manuel Cisneros, Víctor Helí Pinillos, José Cisneros y Francisco Escudero.
		<br>
		El Centro Educativo empieza a funcionar como Colegio Particular de Instrucción Primaria, en un local en la calle Independencia, con 
		56 alumnas y teniendo como Directora a la Sra. Julia Bucley.
		En abril de 1952 por Resolución Ministerial se amplía su funcionamiento con secundaria común.<br>
		
		Gracias a la donación de un terreno y con el aporte económico de la Congregación y de los Padres de Familia, 
		se inicia la construcción del local que hoy ocupa el Plantel y el 1° de abril de 1954 se inician las clases con 
		140 alumnas y hasta el 3ro de Secundaria.<br>

		En 1956 egresa la primera promoción compuesta por 16 alumnas. En 1959 se funda la Escuela Gratuita con 53 niñas y la ayuda del Estado con dos maestras.
		En 1969, luego del Concilio Vaticano II y las Reuniones de los Obispos en Medellín y Puebla, se efectúa la fusión de la Escuela Gratuita con el Colegio, 
		período a partir del cual se incrementa la ayuda económica por parte del Estado.<br>
		En 1982, siguiendo las orientaciones de los Obispos Latinoamericanos que exhortan a optar preferentemente por los jóvenes y especialmente por los 
		menos favorecidos, el Colegio cambia su identidad y se convierte en Parroquial. Este cambio permite seguir recibiendo ayuda económica del Estado, 
		lo que ha facilitado abrir las puertas a más niñas y brindar un mayor servicio a la zona de influencia que comprende las Urbanizaciones de: 
		Los Granados, Daniel Hoyle, Rázuri, La Rinconada, La Noria, El Bosque, Santo Dominguito, Pesqueda, Urbanización La Libertad, Santa Teresa de Avila, 
		Chimú, La Marquesa, Pay Pay, Los Jardines y los distritos: El Porvenir, Laredo, Florencia de Mora y la Avenida Santa, Aranjuez, Unión, etc.<br>
		El mayor beneficio se ha manifestado en el aspecto económico con la creación de pensiones escalonadas y Becas que favorecen a las familias de los 
		lugares con menos recursos económicos y que constituyen  un  estímulo para mejorar el rendimiento académico.<br>
		Durante 15 años se atendió a las Niñas de Primaria, con desayunos, contando en algunas oportunidades con ayuda de Padres de Familia con mejor 
		situación económica.<br>
		A lo largo de los años de vida del Colegio han egresado 57 Promociones y con satisfacción comprobamos que nuestras alumnas tienen buen récord de 
		ingresos a Universidades y Centros Superiores de Estudios. Actualmente tenemos exalumnas ejerciendo con eficiencia, eficacia y responsabilidad su 
		profesión dentro y fuera del país. <br>
		La Congregación Religiosa consecuente con el Ideario de la Iglesia cedió un sector del Colegio para que funcione una Posta de Salud que lleva el 
		nombre de Sagrado Corazón y que luego de varios años de funcionar en nuestro recinto se mudó a su local propio en la Urb. Los Granados.<br>
		El gran regalo a la Comunidad de Los Granados y alrededores, fue que la Congregación cedió al Arzobispado de Trujillo, la hermosa Capilla principal 
		del Colegio, para que sirviera a la comunidad como lo que es hoy: La Parroquia del Sagrado Corazón.<br>
		Finalmente en el año 1998, en Setiembre la Congregación prestó por algunos años el uso del local que anteriormente fue la Escuela gratuita al 
		Programa de Servicio Social de la Universidad Católica y posteriormente al Centro de Atención y Educación de la Familia “CAEF”, 
		con la finalidad de rehabilitar a los niños en abandono y a sus padres, reintegrándolos después a su hogar.<br>
		Durante todos estos años de historia educacional hemos tenido como Directoras a: Hna. María Canales, Hna.Carmen Cubero, Hna.Isabel Bustamente, 
		Hna. Violeta Cornwel, Hna. Fanny Cabreros, Hna. Pilar Cardó, Sra. Raquel Mendiola de Caro, Sra. Rita Alvarado de Linares y la Hna. Lastenia Fernández 
		Maldonado Vigil.<br>
		En la actualidad tenemos como directora a la Hna. Betzabeth Villanueva Tarrillo quien nos acompaña, en este cargo desde el año 2010; siempre fiel al 
		carisma de la Santa Madre nos conduce y nos orienta en la misión de educar y de manifestar el amor del Corazón de Jesús.
